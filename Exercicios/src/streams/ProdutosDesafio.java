package streams;

public class ProdutosDesafio {

	final String nome;
	final double preco;
	final double frete;
	final double desconto;
	
	public ProdutosDesafio(String nome, double preco, double frete, double desconto) {
		this.nome = nome;
		this.preco = preco;
		this.frete = frete;
		this.desconto = desconto;
	}
}
