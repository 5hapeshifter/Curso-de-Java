package controle;

public class WhileDeterminado {

public static void main(String[] args) {
		
		int contador = 1;
		
		while(contador <= 10) {
			System.out.printf("i = %d\n", contador);//%d pra trocar por uma variável inteira
			contador++;//utilizamos o símbolo de imcremento para adicionar um número por vez ao contador
			// contador +=2 pra adicionar de 2 em 2 ou outro valor maior que 1 que você desejar
			
			
		}
	}
}
